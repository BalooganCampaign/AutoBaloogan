        public static string smart_card(string str)
        {
            if (str == "")
                return "";
            string a = str;
            if (a.Length == 3)
            {
                //should only be a 10 here
                a = a.Replace("10", "T");
            }
            if (a.Length != 2)
            {
                throw new Exception("BAD CARD: " + str);
            }
            a = a.Replace("1", "A");
            a = a.Substring(1, 1) + a.Substring(0, 1);
            return a;
        }
