        public static void update_user(baloogan_pokerDB db, Action<string> reply, PokerUser user)
        {
            int total = 0;
            while (user.Chips < 0)
            {
                user.Chips += 100;
                user.Credit += 100;
                total += 100;
            }
            if (user.Credit < 0)
            {
                total -= user.Credit;
                user.Chips -= user.Credit;
                user.Credit = 0;
            }
            if (total > 0)
                reply("AutoBaloogan Casino extends **" + user.Name + "** a `" + total + "฿` loan bringing his stack to `" +
                      user.Chips + "฿`. Total credit extended: `" + user.Credit + "฿`");
            db.Update(user);
            db.Insert(new PokerUsersHistory()
                {
                    Chips = user.Chips,
                    Credit = user.Credit,
                    PokerUserID = user.ID,
                    Moment = DateTime.UtcNow
                });
        }
